package middlewares

import "net/http"

//StudyJSONSetting is middleware for setting each study route
func StudyJSONSetting(next http.Handler) http.Handler {
	return http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		w.Header().Set("Content-Type", "application/json")
		next.ServeHTTP(w, r)
	})
}

